public class One {
    private String a;
    private String b;
    private two t;

    public One() {
    }

    public One(String a, String b, two t) {
        this.a = a;
        this.b = b;
        this.t = t;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }

    public two getT() {
        return t;
    }

    public void setT(two t) {
        this.t = t;
    }
}
